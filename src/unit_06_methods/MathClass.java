package unit_06_methods;

public class MathClass {

	public  static  void  main(String[] args) {
	    System.out.println("cos(90) = " + Math.cos(90));
	    System.out.println("sin(90) = " + Math.sin(90));
	    System.out.println("ceil(4.2) = " + Math.ceil(4.2));
	    System.out.println("floor(4.2) = " + Math.floor(4.2));
	    System.out.println("abs(-5.8) = " + Math.abs(-5.8));
	    System.out.println("abs(5) = " + Math.abs(5));
	    System.out.println("pow(2,3) = " + Math.pow(2, 3));
	    System.out.println("pow(8,0.5) = " + Math.pow(8, 0.5));
	    System.out.println("sqrt(16) = " + Math.sqrt(16));
	    System.out.println("sqrt(18) = " + Math.sqrt(18));
	}
}